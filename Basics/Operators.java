
public class Operators {
    public static void main(String[] args) {
        // Arithmetic Operators -> + , - , % , / , *, ++, --
        double num1, num2, exp;
        num1 = 20;
        num2 = 23;

        exp = num1 + num2;
        exp = num1 - num2;
        exp = 20 % 23;
        exp = 20 / 2;
        exp = num1++;
        exp = ++num1;
        exp = num1--;
        exp = --num1;

        // Assignment Operators -> =, +=,-=,*=,/=,%=
        // Comparision Operators -> ==, <=, >=, !=, <, >
        // Logical Operators -> &&, ||, !
    }
}
